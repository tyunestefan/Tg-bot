async def search(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.message.text.strip()

    if query == "🏙 Топ городов":
        await top_cities(update, context)
        return
    if query == "🌍 Топ стран":
        await top_countries(update, context)
        return
    if query == "ℹ️ Помощь":
        await help_cmd(update, context)
        return

    key = query.lower()
    if key in POPULATION_DATA:
        name, population = POPULATION_DATA[key]
        await update.message.reply_text(
            f"📍 *{name}*\n\n"
            f"👥 Население: *{population} чел.*",
            parse_mode="Markdown"
        )
    else:
        await update.message.reply_text(
            f"❌ Не нашёл *«{query}»* в базе.\n\n"
            "Попробуй другое название или напиши на английском.\n"
            "Нажми *ℹ️ Помощь* для примеров.",
            parse_mode="Markdown"
        )


